
public class Dimension{

private double dL,dR,dH,x,y;

public Dimension(double dL,double dR,double dH,double x,double y)
 {
this.dL=dL;
this.dR=dR;
this.dH=dH;
this.x=x;
this.y=y;
 
 }

public double getdL()
{
return this.dL;}

public double getdR()
{
return this.dR;}

public double getdH()
{
return this.dH;}

public double getX()
{
return this.x;}

public double getY()
{
return this.y;}

public void setdL(double dl)
{
	this.dL=dl;
}
public void setdR(double dr)
{
	this.dR=dr;
}
public void setdH(double dh)
{
	this.dH=dh;
}
public void setx(double x)
{
	this.x=x;
}
public void sety(double y)
{
	this.y=y;
}
}//End of Dimension

